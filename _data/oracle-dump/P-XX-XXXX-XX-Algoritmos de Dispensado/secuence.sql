DECLARE
  DIFFERENCE INTEGER;
  SQLSTMT VARCHAR2(255);
  SEQUENCEVALUE NUMBER;
BEGIN

SQLSTMT := 'ALTER SEQUENCE ALGORITHM_SEQ INCREMENT BY ';
SELECT  MAX(PK_ID) INTO DIFFERENCE FROM TBL_DISPENSED_ALGORITHM;
EXECUTE IMMEDIATE SQLSTMT || DIFFERENCE;
SELECT CAN.ALGORITHM_SEQ.NEXTVAL INTO SEQUENCEVALUE FROM DUAL;  
EXECUTE IMMEDIATE SQLSTMT || 1;

END;
