DECLARE
  DIFFERENCE INTEGER;
  SQLSTMT VARCHAR2(255);
  SEQUENCEVALUE NUMBER;
BEGIN

SQLSTMT := 'ALTER SEQUENCE MENU_SEQ INCREMENT BY ';
SELECT  MAX(PK_ID) INTO DIFFERENCE FROM TBL_MENU;
EXECUTE IMMEDIATE SQLSTMT || DIFFERENCE;
SELECT CAN.MENU_SEQ.NEXTVAL INTO SEQUENCEVALUE FROM DUAL;  
EXECUTE IMMEDIATE SQLSTMT || 1;

END;
