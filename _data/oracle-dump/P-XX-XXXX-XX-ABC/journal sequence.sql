DECLARE
  DIFFERENCE INTEGER;
  SQLSTMT VARCHAR2(255);
  SEQUENCEVALUE NUMBER;
BEGIN

SQLSTMT := 'ALTER SEQUENCE JOURNAL_SEQ INCREMENT BY ';
SELECT  MAX(PK_ID_JOURNAL) INTO DIFFERENCE FROM TBL_JOURNAL;
EXECUTE IMMEDIATE SQLSTMT || DIFFERENCE;
SELECT CAN.JOURNAL_SEQ.NEXTVAL INTO SEQUENCEVALUE FROM DUAL;  
EXECUTE IMMEDIATE SQLSTMT || 1;

END;
